var getTextureByName = function (name, scene) {
    // "this" is forbidden since this code is exported directly
    for (var i = 0; i < scene.textures.length; i++) {
        var texture = scene.textures[i];
        if (texture.name === name) {
            return texture;
        }
    }
    return null;
}

